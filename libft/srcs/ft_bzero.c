#include <string.h>

void				ft_bzero(void *b, size_t len)
{
	size_t			i;
	unsigned char	s;

	s = 0;
	i = 0;
	while (i < len)
	{
		((unsigned char *)b)[i] = s;
		i++;
	}
}
